def square(x, y):
    "Draw square using path at (x, y)."
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

def offset(point):
    "Return offset of point in tiles."
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index

def valid(point):
    "Return True if point is valid in tiles."
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0

def world():
    "Draw world using path."
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')

def move():
    """Move pacman and all ghosts."""
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    for ghost in ghosts:
        point, _ = ghost

        x_diff = pacman.x - point.x
        y_diff = pacman.y - point.y

        if abs(x_diff) > abs(y_diff):
            if x_diff > 0:
                direction = vector(7, 0)  
            else:
                direction = vector(-7, 0)  
        else:
            if y_diff > 0:
                direction = vector(0, 7)  
            else:
                direction = vector(0, -7)  

        if valid(point + direction):
            point.move(direction)
        else:
            if abs(x_diff) > abs(y_diff):
                if y_diff > 0:
                    alt_direction = vector(0, 7) 
                else:
                    alt_direction = vector(0, -7)  
            else:
                if x_diff > 0:
                    alt_direction = vector(7, 0)  
                else:
                    alt_direction = vector(-7, 0)  

            if valid(point + alt_direction):
                point.move(alt_direction)

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    # Check for collision with Pac-Man
    for point, _ in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 100)

def change(x, y):
    "Change pacman aim if valid."
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y

